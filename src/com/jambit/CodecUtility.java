package com.jambit;


/**
 * CodecUtility is an object used to save the seed and key
 */
public class CodecUtility {

    private int key;
    private int seed;

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public int getSeed() {
        return seed;
    }

    public void setSeed(int seed) {
        this.seed = seed;
    }
}
